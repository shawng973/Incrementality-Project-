"use client";

import { useState } from "react";
import { Sparkles, RefreshCw } from "lucide-react";
import { Button } from "@/components/ui/button";
import { api } from "@/lib/api";
import type { NarrativeResponse } from "@/types";

interface NarrativeDisplayProps {
  testId: string;
  jobId: string;
  /** Initial narrative if already generated (passed from server). */
  initial?: NarrativeResponse | null;
}

/**
 * Renders LLM narrative markdown with basic formatting.
 * Handles ## headings and **bold** without an external library.
 */
function MarkdownBody({ markdown }: { markdown: string }) {
  const sections = markdown.split(/\n(?=## )/);

  return (
    <div className="space-y-4 text-sm text-gray-700">
      {sections.map((section, i) => {
        const lines = section.split("\n");
        const firstLine = lines[0];
        const isHeading = firstLine.startsWith("## ");
        const heading = isHeading ? firstLine.replace(/^## /, "") : null;
        const body = isHeading ? lines.slice(1).join("\n").trim() : section.trim();

        return (
          <div key={i}>
            {heading && (
              <h3 className="mb-1 text-sm font-semibold text-gray-900">{heading}</h3>
            )}
            {body && (
              <p
                className="whitespace-pre-wrap leading-relaxed"
                dangerouslySetInnerHTML={{
                  __html: body
                    .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
                    .replace(/^- /gm, "• "),
                }}
              />
            )}
          </div>
        );
      })}
    </div>
  );
}

export function NarrativeDisplay({
  testId,
  jobId,
  initial = null,
}: NarrativeDisplayProps) {
  const [narrative, setNarrative] = useState<NarrativeResponse | null>(initial);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  async function generate() {
    setIsLoading(true);
    setError(null);
    try {
      const result = await api.narrative.generate(testId, jobId);
      setNarrative(result);
    } catch {
      setError("Failed to generate narrative. Please try again.");
    } finally {
      setIsLoading(false);
    }
  }

  if (!narrative) {
    return (
      <div className="flex flex-col items-center gap-3 py-6 text-center">
        <Sparkles className="h-8 w-8 text-brand-300" />
        <p className="text-sm text-gray-500">
          Generate a plain-English interpretation of these results.
        </p>
        {error && (
          <p role="alert" className="text-sm text-red-600">
            {error}
          </p>
        )}
        <Button
          onClick={generate}
          isLoading={isLoading}
          variant="secondary"
          size="sm"
        >
          <Sparkles className="h-3.5 w-3.5" />
          Generate narrative
        </Button>
      </div>
    );
  }

  return (
    <div>
      <div className="mb-3 flex items-center justify-between">
        <p className="text-xs text-gray-400">
          Generated by <span className="font-medium">{narrative.model}</span> ·{" "}
          {narrative.prompt_tokens + narrative.completion_tokens} tokens
        </p>
        <Button
          onClick={generate}
          isLoading={isLoading}
          variant="ghost"
          size="sm"
          aria-label="Regenerate narrative"
        >
          <RefreshCw className="h-3.5 w-3.5" />
          Regenerate
        </Button>
      </div>

      {error && (
        <p role="alert" className="mb-3 text-sm text-red-600">
          {error}
        </p>
      )}

      <MarkdownBody markdown={narrative.body_markdown} />
    </div>
  );
}
